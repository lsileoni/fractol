#include "../libft.h"

void	ft_bzero(void *s, size_t n)
{
	int				i;
	unsigned char	*p;

	p = (unsigned char *)s;
	if (n)
	{
		i = 0;
		while (n--)
		{
			p[i] = 0;
			i++;
		}
	}
}
